
package hasmap;

public class Studnts {
  
   private String name; 
   private String Email;
   private int id;

    public Studnts() {
    }

    public Studnts(String name, String Email, int id) {
        this.name = name;
        this.Email = Email;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }
   
   
    
    
    
    
    
    
    
    
    
    
}
